"""datatypes in python
This module contains the datatypes used in the Genainotes application.
- int: Represents integer values.
- float: Represents floating-point numbers.
- str: Represents string values.
- bool: Represents boolean values (True or False).
- list: Represents a mutable sequence of values.
- tuple: Represents an immutable sequence of values.
- dict: Represents a collection of key-value pairs.
- set: Represents an unordered collection of unique values."""

"""examples each datatype"""
# Integer
def example_int() -> int:
    """Returns an example integer."""
    return 42
# Float
def example_float() -> float:
    """Returns an example floating-point number."""
    return 3.14
# String
def example_str() -> str:
    """Returns an example string."""
    return "Hello, World!"
# Boolean
def example_bool() -> bool:
    """Returns an example boolean value."""
    return True
# List
def example_list() -> List[int]:
    """Returns an example list of integers."""
    return [1, 2, 3, 4, 5]
# Tuple
def example_tuple() -> Tuple[str, int]:
    """Returns an example tuple containing a string and an integer."""
    return ("example", 10)
# Dictionary
def example_dict() -> Dict[str, int]:
    """Returns an example dictionary with string keys and integer values."""
    return {"one": 1, "two": 2, "three": 3}
# Set
def example_set() -> Set[str]:
    """Returns an example set of strings."""
    return {"apple", "banana", "cherry"}

"""coding style examples for each datatype"""
